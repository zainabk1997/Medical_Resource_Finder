    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:

        long communityID = Long.parseLong(txtCommunityID.getText());
        String communityName = txtCommunityName.getText();
        long communityZip = Long.parseLong(txtCommunityZip.getText());

        Community community = new Community(communityID, communityName, communityZip);

        SystemAdmin.communityDirectoryRef.getCommunityDirectory().add(community);

        JOptionPane.showMessageDialog(this, "New Hospital Details Added Successfully!");

        txtCommunityID.setText("");
        txtCommunityName.setText("");
        txtCommunityZip.setText("");

        populateTable();
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        int selected = tblCommunities.getSelectedRow();

        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to update!");
            return;
        }

        long communityID = Long.parseLong(txtCommunityID.getText());
        String communityName = txtCommunityName.getText();
        long communityZip = Long.parseLong(txtCommunityZip.getText());

        //      City city = new City(cityName, cityID, communityNames);
        Community community = SystemAdmin.communityDirectoryRef.getCommunityDirectory().get(selected);

        community.setCommunityID(communityID);
        community.setCommunityName(communityName);
        community.setCommunityZip(communityZip);

        populateTable();

        txtCommunityID.setText("");
        txtCommunityName.setText("");
        txtCommunityZip.setText("");
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selected = tblCommunities.getSelectedRow();

        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to view!");
            return;
        }

        Community community = SystemAdmin.communityDirectoryRef.getCommunityDirectory().get(selected);

        txtCommunityID.setText(String.valueOf(community.getCommunityID()));
        txtCommunityName.setText(String.valueOf(community.getCommunityName()));
        txtCommunityZip.setText(String.valueOf(community.getCommunityZip()));
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int selected = tblCommunities.getSelectedRow();

        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete!");
            return;
        }

        Community community = SystemAdmin.communityDirectoryRef.getCommunityDirectory().get(selected);

        SystemAdmin.communityDirectoryRef.getCommunityDirectory().remove(community);

        populateTable();

        txtCommunityID.setText("");
        txtCommunityName.setText("");
        txtCommunityZip.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed


