private void populateTable() {
          DefaultTableModel model = (DefaultTableModel) tblCommunities.getModel();
        model.setRowCount(0);
                for (Community community : SystemAdmin.communityDirectoryRef.getCommunityDirectory()) {

            Object[] rowData = new Object[3];
            rowData[0] = community.getCommunityID();
            rowData[1] = community.getCommunityName();
            rowData[2] = community.getCommunityZip();
            System.out.println(community.getCommunityZip());
            model.addRow(rowData);

        }
    }
