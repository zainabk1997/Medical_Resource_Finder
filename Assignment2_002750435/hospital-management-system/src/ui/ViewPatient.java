    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblViewPatients.getModel();
        model.setRowCount(0);
                for (PatientDetails patientDetails : SystemAdmin.patientDirectoryRef.getPatientDirectory()) {

            Object[] rowData = new Object[7];
            rowData[0] = patientDetails.getPersonFullName();
            rowData[1] = patientDetails.getPersonAge();
            rowData[2] = patientDetails.getPersonGender();
            rowData[3] = patientDetails.getPersonPhoneNumber();
            rowData[4] = patientDetails.getHouse().getHouseNumber();
            rowData[5] = patientDetails.getHouse().getStreetAddress();
            rowData[6] = patientDetails.getHouse().getHouseZip();
            model.addRow(rowData);

        }
    }
