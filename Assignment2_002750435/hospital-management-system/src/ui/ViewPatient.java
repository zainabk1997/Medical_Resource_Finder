    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selected = tblViewPatients.getSelectedRow();
        
          if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to view!");
            return;
        }
        
        PatientDetails patient = SystemAdmin.patientDirectoryRef.getPatientDirectory().get(selected);
        
        txtSSN.setText(String.valueOf(patient.getPersonSSN()));
        txtFullName.setText(String.valueOf(patient.getPersonFullName()));
        txtAge.setText(String.valueOf(patient.getPersonAge()));
        String personGender = patient.getPersonGender();
        if(personGender.equals("Female")){
            female.setSelected(true);
            male.setSelected(false);
            nonBinary.setSelected(false);
        } else if (personGender.equals("Male")){
        male.setSelected(true);
        female.setSelected(false);
            nonBinary.setSelected(false);
    } else {
            nonBinary.setSelected(true);
            male.setSelected(false);
            female.setSelected(false);
        }
        txtPhoneNumber.setText(String.valueOf(patient.getPersonPhoneNumber()));
        txtHouseNumber.setText(String.valueOf(patient.getHouse().getHouseNumber()));
        txtStreetAddress.setText(String.valueOf(patient.getHouse().getStreetAddress()));
        txtZipCode.setText(String.valueOf(patient.getHouse().getHouseZip()));
        txtUserName.setText(String.valueOf(patient.getUserName()));
        btnDelete.setEnabled(true);
        btnUpdate.setEnabled(true);
        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selected = tblViewPatients.getSelectedRow();
        
        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete!");
            return;
        }
        
        PatientDetails patient = SystemAdmin.patientDirectoryRef.getPatientDirectory().get(selected);
        
        SystemAdmin.patientDirectoryRef.getPatientDirectory().remove(patient);
        SystemAdmin.personDirectoryRef.getPersonDirectory().remove(patient);
        
        populateTable();
        
        txtSSN.setText("");
        txtFullName.setText("");
        txtAge.setText("");
        genderButtonGroup.clearSelection();
        txtPhoneNumber.setText("");
        txtHouseNumber.setText("");
        txtStreetAddress.setText("");
        txtZipCode.setText("");
        txtUserName.setText("");
        
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selected = tblViewPatients.getSelectedRow();
        
        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to update!");
            return;
        }
        
        long patientSSN = Long.parseLong(txtSSN.getText());
        String patientFullName = txtFullName.getText();
        int patientAge = Integer.parseInt(txtAge.getText());
        String patientGender="";
        if(male.isSelected()){
            patientGender = "Male";
            
        } else if(female.isSelected()){
            patientGender = "Female";
            
        } else {
            patientGender = "Non Binary";
            
        }
        long patientPhone = Long.parseLong(txtPhoneNumber.getText());
        
        int houseNumber = Integer.parseInt(txtHouseNumber.getText());
        
        long houseZip = Long.parseLong(txtZipCode.getText());
        
        String patientAddress = txtStreetAddress.getText();
        
        House house = new House(patientAddress, houseNumber, houseZip);
        String patientUserName = txtUserName.getText();
        
        PatientDetails patient = SystemAdmin.patientDirectoryRef.getPatientDirectory().get(selected);
        
        
        patient.setPersonSSN(patientSSN);
        patient.setPersonFullName(patientFullName);
        patient.setPersonAge(patientAge);
        patient.setPersonGender(patientGender);   
        patient.setPersonPhoneNumber(patientPhone);
        patient.setHouse(house);
        patient.setUserName(patientUserName);
        
          populateTable();
          
        txtSSN.setText("");
        txtFullName.setText("");
        txtAge.setText("");
        genderButtonGroup.clearSelection();
        txtPhoneNumber.setText("");
        txtHouseNumber.setText("");
        txtStreetAddress.setText("");
        txtZipCode.setText("");
        txtUserName.setText("");
        
    }//GEN-LAST:event_btnUpdateActionPerformed


