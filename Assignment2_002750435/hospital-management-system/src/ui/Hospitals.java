    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblHospitals.getModel();
        model.setRowCount(0);
                for (Hospital hospital : SystemAdmin.hospitalDirectoryRef.getHospitalDirectory()) {

            Object[] rowData = new Object[3];
            rowData[0] = hospital.getHospitalID();
            rowData[1] = hospital.getHospitalName();
            rowData[2] = hospital.getCommunityZip();
            model.addRow(rowData);

        }
    }
