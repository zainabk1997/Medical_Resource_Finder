    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int selected = tblHospitals.getSelectedRow();

        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete!");
            return;
        }

        Hospital hospital = SystemAdmin.hospitalDirectoryRef.getHospitalDirectory().get(selected);

        SystemAdmin.hospitalDirectoryRef.getHospitalDirectory().remove(hospital);

        populateTable();

        txtHospitalID.setText("");
        txtHospitalName.setText("");
        txtCommunityZip.setText("");

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selected = tblHospitals.getSelectedRow();

        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to view!");
            return;
        }

        Hospital hospital = SystemAdmin.hospitalDirectoryRef.getHospitalDirectory().get(selected);

        txtHospitalID.setText(String.valueOf(hospital.getHospitalID()));
        txtHospitalName.setText(String.valueOf(hospital.getHospitalName()));
        txtCommunityZip.setText(String.valueOf(hospital.getCommunityZip()));
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        int selected = tblHospitals.getSelectedRow();

        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to update!");
            return;
        }

        long hospitalID = Long.parseLong(txtHospitalID.getText());
        String hospitalName = txtHospitalName.getText();
        long communityZip = Long.parseLong(txtCommunityZip.getText());

        //      City city = new City(cityName, cityID, communityNames);
        Hospital hospital = SystemAdmin.hospitalDirectoryRef.getHospitalDirectory().get(selected);

        hospital.setHospitalID(hospitalID);
        hospital.setHospitalName(hospitalName);
        hospital.setCommunityZip(communityZip);

        populateTable();

        txtHospitalID.setText("");
        txtHospitalName.setText("");
        txtCommunityZip.setText("");
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:

        long hospitalID = Long.parseLong(txtHospitalID.getText());
        String hospitalName = txtHospitalName.getText();
        long communityZip = Long.parseLong(txtCommunityZip.getText());

        Hospital hospital = new Hospital(hospitalID, hospitalName, communityZip);

        SystemAdmin.hospitalDirectoryRef.getHospitalDirectory().add(hospital);

        JOptionPane.showMessageDialog(this, "New Hospital Details Added Successfully!");

        txtHospitalID.setText("");
        txtHospitalName.setText("");
        txtCommunityZip.setText("");

        populateTable();
    }//GEN-LAST:event_btnCreateActionPerformed


